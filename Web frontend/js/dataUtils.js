

function get_avgdist(){
    const baseline = 22265
    let result = []
    let noise = [1032.665575875496, 1754.0952666350295, 1854.0784213475993, 632.1199808627526, 1484.8413501901146, 872.6731551203226, 1815.7635273749077, 1123.9285543764295, 9.959555174607893, 19.073140727478677, 1796.4958909622837, 728.4571479432365, 599.2307555799972, 1959.981899174261]
    c = 0
    for(let i= 0.4; i < 2.4; i += 0.15){
        let x = (i - 2)
        let val = 0.2* (x*x*x+2*x*x)
        result.push(baseline / 1.5 + baseline * val - 0.2*noise[c])
        c += 1;
    }
    return result
}

